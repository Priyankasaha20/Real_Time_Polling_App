app.use("/api/auth", authLimiter, authRoutes);
app.use("/api/polls", pollRoutes);
app.use("/api/votes", voteLimiter, voteRoutes);

// â”€â”€â”€ Socket.io â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
initSocket(server);

